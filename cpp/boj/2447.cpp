#include <iostream>
using namespace std;

int n;

void star(int modN, int i, int j) {
  if ((i / modN) % 3 == 1 && (j / modN) % 3 == 1)
    cout << " ";
  else {
    if (modN / 3 == 0)
      cout << "*";
    else
      star(modN / 3, i, j);
  }
}

int main(void) {
  cin >> n;
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < n; j++) star(n, i, j);
    cout << "\n";
  }
  return 0;
}